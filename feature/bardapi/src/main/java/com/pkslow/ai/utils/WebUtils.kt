package com.pkslow.ai.utils

import okhttp3.OkHttpClient
import java.time.Duration

object WebUtils {
    fun okHttpClientWithTimeout(timeout: Duration): OkHttpClient {
        return OkHttpClient.Builder()
            .callTimeout(timeout)
            .readTimeout(timeout)
            .connectTimeout(timeout)
            .build()
    }
}